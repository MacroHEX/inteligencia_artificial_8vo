class Entidad:
    def __init__(self, nombre, tipo, ruc=None, direccion=None, telefono=None, email=None, cedula=None):
        self.nombre = nombre
        self.tipo = tipo
        self.ruc = ruc
        self.direccion = direccion
        self.telefono = telefono
        self.email = email
        self.cedula = cedula
